    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create(Request $request)
    {
        $currentuser= JWTAuth::parseToken()->authenticate();
        $vehicles= new Vehicle();
        $vehicles->company_id=$currentuser->company_id;
        $vehicles->plate_no=$request->plate_no;
        $vehicles->capacity=$request->capacity;
        if($vehicles->save()){
            return response()->json(['status'=>'ok','token'=>'ok'],201);
        }

    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        Vehicle::create([
           'company_id' => auth()->user()->company_id,
           'plate_no' => $request->plate_no,
           'capacity' => $request->capacity
        ]);
        $message = "you have successfully registered your vehicle.";
        session()->regenerate();
        session()->flash('saved',$message);
        return redirect()->back();
    }

